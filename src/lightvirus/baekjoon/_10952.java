package lightvirus.baekjoon;

import java.util.Scanner;

public class _10952 {
    public static void main (String [] args) {
        Scanner sc = new Scanner(System.in);

        int a, b;

        while(1==1) {
            a = sc.nextInt();
            b = sc.nextInt();
            if (a == 0 && b == 0) break;
            else System.out.println(a + b);
        }
    }
}
